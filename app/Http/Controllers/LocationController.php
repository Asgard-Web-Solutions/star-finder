    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (Gate::denies('manage-game-elements')) {
            Alert::toast('Permission Denied', 'warning');
            return redirect('/');
        }

        $locations = Location::all();
        $grid = array();

        foreach ($locations as $location) {
            $grid[$location->x][$location->y]['id'] = $location->system_id;
            $grid[$location->x][$location->y]['name'] = ($location->system_id) ? $location->system->name : "Null Space";
        }

        return view('location.index', [
            'locations' => $locations,
            'grid'      => $grid,
        ]);
    }

